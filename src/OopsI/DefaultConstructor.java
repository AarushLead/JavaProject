package OopsI;

public class DefaultConstructor {
	int a;
	int b;
	boolean flag;

	public static void main(String[] args) {
		DefaultConstructor dc=new DefaultConstructor();
		System.out.println();
		System.out.println("a = " + dc.a);
        System.out.println("b = " + dc.b);
        System.out.println("flag = " + dc.flag);
	}

}
